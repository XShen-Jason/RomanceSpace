const fs = require('fs');
const path = require('path');

const projectsDir = path.join(__dirname, '../src/projects');
const registryFile = path.join(__dirname, '../src/registry.js');

function generateRegistry() {
    console.log('[Auto-Registry] Scanning for project templates...');

    if (!fs.existsSync(projectsDir)) {
        console.warn(`[Auto-Registry] Warning: Directory not found -> ${projectsDir}`);
        // Create an empty registry if the directory doesn't exist
        fs.writeFileSync(registryFile, `export const PROJECT_REGISTRY = {};\n`, 'utf8');
        return;
    }

    const entries = fs.readdirSync(projectsDir, { withFileTypes: true });
    const templates = entries
        .filter(dirent => dirent.isDirectory())
        .map(dirent => dirent.name);

    let output = `/**\n * AUTO-GENERATED REGISTRY FILE\n * Do not edit this file manually.\n * It is generated by scripts/generate-registry.js at build time.\n */\n\n`;

    // Dynamically generate import statements using safe variable names (t0, t1, t2...)
    templates.forEach((t, index) => {
        // Check if index.js exists in the template directory to avoid broken builds
        const indexPath = path.join(projectsDir, t, 'index.js');
        if (fs.existsSync(indexPath)) {
            output += `import * as t${index} from './projects/${t}/index.js';\n`;
        } else {
            console.warn(`[Auto-Registry] Skipping '${t}', missing index.js entry point.`);
        }
    });

    output += `\nexport const PROJECT_REGISTRY = {\n`;
    templates.forEach((t, index) => {
        const indexPath = path.join(projectsDir, t, 'index.js');
        if (fs.existsSync(indexPath)) {
            output += `  '${t}': t${index},\n`;
        }
    });
    output += `};\n`;

    fs.writeFileSync(registryFile, output, 'utf8');
    console.log(`[Auto-Registry] Successfully generated registry for ${templates.length} templates: ${templates.join(', ')}`);
}

generateRegistry();
